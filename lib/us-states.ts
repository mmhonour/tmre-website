/**
 * Return the two-letter USPS abbreviation for a US state. Accepts either a full
 * state name ("Connecticut") or an already-abbreviated code ("CT"), and passes
 * through any unrecognized value unchanged.
 */
export function abbreviateUsState(
  state: string | null | undefined,
): string {
  if (!state) return "";
  const trimmed = state.trim();
  if (!trimmed) return "";
  if (trimmed.length === 2) return trimmed.toUpperCase();
  return US_STATE_ABBREVIATIONS[trimmed.toLowerCase()] ?? trimmed;
}
